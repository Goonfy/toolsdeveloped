package org.academiadecodigo.gnunas;

public class User {
    private String Name;
    private String Email;

    public void setName(String name) {
        Name = name;
    }

    public void setEmail(String email) {
        Email = email;
    }

    public String getName() {
        return Name;
    }

    public String getEmail() {
        return Email;
    }
}
